// CMy3172View 消息处理程序


void CMy3172View::OnKeyDown(UINT nChar, UINT nRepCnt, UINT nFlags)
{
	// TODO: Add your message handler code here and/or call default
	CMy3172Doc* pDoc = GetDocument();

	switch (nChar)
	{
	case VK_LEFT:{
		pDoc->cr.left -= 3;
		pDoc->cr.right -= 3;
	}break;
	case VK_RIGHT:{
		pDoc->cr.left += 3;
		pDoc->cr.right += 3;
	}break;

	case VK_UP:{
		pDoc->cr.top -= 3;
		pDoc->cr.bottom -= 3;
	}break;

	case VK_DOWN:{
		pDoc->cr.top += 3;
		pDoc->cr.bottom += 3;
	}break;

	case VK_HOME:{
		pDoc->cr.top -= 3;
		pDoc->cr.left -= 3;
	}break;
	case VK_END:{
		pDoc->cr.bottom += 3;
		pDoc->cr.right += 3;
	}break;
	}
	InvalidateRect(NULL, TRUE);

	CView::OnKeyDown(nChar, nRepCnt, nFlags);
}


void CMy3172View::OnLButtonDown(UINT nFlags, CPoint point)
{
	// TODO: Add your message handler code here and/or call default
	CMy3172Doc* pDoc = GetDocument();
	pDoc->cr.top = 200; pDoc->cr.left = 500; pDoc->cr.bottom = 300; pDoc->cr.right = 600;
	InvalidateRect(NULL, TRUE);
	CView::OnLButtonDown(nFlags, point);
}
